NUM_COLORS = len(STANDARD_COLORS)


def from_colorname_to_bgr(color: str):
    r""" Maps a color name to a certain bgr value. """
    rgb_color = webcolors.name_to_rgb(color)
    result = (rgb_color.blue, rgb_color.green, rgb_color.red)
    return result


def num2bgr(num: int):
    """ 
    Maps an integer to a certain bgr value. 
    If value exceeds the number of colors, it will start from the beginning.
    """
    _i = num % NUM_COLORS
    return from_colorname_to_bgr(STANDARD_COLORS[_i])
